<h2>LFI Test Alani</h2>
<p>Bu bolum, dosya yukleme mekanizmasi ve girdi filtreleme davranisini gozlemlemek icin ayrilmistir.</p>
<p>Parametre manipule edilirken uygulamanin dosya secim akisini dikkatle izlemeniz beklenir.</p>
<p>Gozlenen bulgulari tekrar uretilebilir adimlarla teknik rapor formatinda dokumante edin.</p>

<h3>Lab Checklist</h3>
<ul>
    <li>Girdi normalizasyon adimlarini gozlemle</li>
    <li>Farkli payload desenlerinin etkisini karsilastir</li>
    <li>Bulgu etkisini teknik olarak dogrula</li>
